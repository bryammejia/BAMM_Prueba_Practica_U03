    private void actualizarTabla(){
        String [] encabezado = new String[4];
        encabezado[0]="CODIGO";
        encabezado[0]="TITULO";
        encabezado[0]="DIRECTOR";
        encabezado[0]="N. ACTORES";
        
        var datos = new Object[this.peliculaControl.listar().size()][4];
        var i=0;
        for(var pelicula:this.peliculaControl.listar()){
            datos[i][0]=pelicula.getCodigo();
            datos[i][1]=pelicula.getTitulo();
            datos[i][2]=pelicula.getDirector();
            datos[i][3]=pelicula.getNumeroActores();
            i++;
            
        }
        this.modeloTabla= new DefaultTableModel(datos, encabezado);
        this.tblPelicula.setModel(modeloTabla);
    }
    
